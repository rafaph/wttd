from datetime import datetime


class DatetimesMixin(object):
    datetimes = [
        datetime(2019, 11, 23, 9, 59, 59),
        datetime(2019, 11, 23, 11, 00, 0),
        datetime(2019, 11, 23, 16, 00, 0),
        datetime(2019, 11, 24, 14, 00, 1),
        datetime(2019, 11, 24, 16, 00, 1),
        datetime(2019, 11, 25, 8, 00, 9)
    ]
